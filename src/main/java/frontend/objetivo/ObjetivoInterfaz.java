    private void cargarDatos(Proyecto proyecto, Objetivo objetivo, boolean ver) {
        this.proyecto = proyecto;
        this.objetivo = objetivo;

        if (objetivo != null) {
            this.txtNombre.setText(objetivo.getNombre());
            this.txtDescripcion.setText(objetivo.getDescripcion());
            btnCrear.setText("Modificar");
            jLabel1.setText("Modificar Objetivo");
            setTitle("Modificar Objetivo");
        }

        if (ver) {
            this.txtNombre.setEditable(false);
            this.txtDescripcion.setEditable(false);
            this.btnCrear.setVisible(false);
            jLabel1.setText("Detalles Objetivo");
            setTitle("Ver Objetivo");
        }
    }

    private void initControladores() {
        this.objetivoControlador = new ObjetivoControlador();
    }

