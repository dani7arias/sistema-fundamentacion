    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnCrearActionPerformed
        String nombre = txtNombre.getText();
        String descripcion = txtDescripcion.getText();

        if (nombre.isEmpty() || descripcion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor llene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (objetivo == null) {
            objetivo = new Objetivo();
            objetivo.setNombre(nombre);
            objetivo.setDescripcion(descripcion);
            objetivo.setIdProyecto(proyecto.getId());
            objetivoControlador.insertar(objetivo);
            JOptionPane.showMessageDialog(this, "Objetivo creado correctamente", "Objetivo Creado",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            objetivo.setNombre(nombre);
            objetivo.setDescripcion(descripcion);
            objetivoControlador.actualizar(objetivo);
            JOptionPane.showMessageDialog(this, "Objetivo modificado correctamente", "Objetivo Modificado",
                    JOptionPane.INFORMATION_MESSAGE);
        }
        this.dispose();
    }// GEN-LAST:event_btnCrearActionPerformed

