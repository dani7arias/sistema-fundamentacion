    private void cargarDatos(Proyecto proyecto, Alternativa alternativa, boolean evaluar) {
        this.proyecto = proyecto;
        if (alternativa != null) {
            this.alternativa = alternativa;
            txtCategoria.setModel(
                    new javax.swing.DefaultComboBoxModel<>(new String[] { alternativa.getCategoria() }));
            txtCategoria.setSelectedItem(alternativa.getCategoria());
            txtNombre.setText(alternativa.getNombre());
            txtDescripcion.setText(alternativa.getDescripcion());
            txtPros.setText(alternativa.getPros());
            txtContras.setText(alternativa.getContras());
            txtCosto.setText(alternativa.getCosto().toString());
            txtImplementacion.setText(alternativa.getTiempoDeImplementacion().toString());
            txtCategoria.setSelectedItem(alternativa.getCategoria());
            btnCrear.setText("Modificar");
            jLabel2.setText("Modificar Alternativa");
            this.setTitle("Modificar Alternativa");
        }

        if (evaluar) {
            this.evaluar = evaluar;
            txtCategoria.setModel(
                    new javax.swing.DefaultComboBoxModel<>(new String[] { "Funcional", "Pendiente", "Rechazada" }));
            btnCrear.setText("Evaluar");
            jLabel2.setText("Evaluar Alternativa");
            this.setTitle("Evaluar Alternativa");
            txtCategoria.setSelectedItem(alternativa.getCategoria());
            txtNombre.setEditable(false);
            txtDescripcion.setEditable(false);
            txtPros.setEditable(false);
            txtContras.setEditable(false);
            txtCosto.setEditable(false);
            txtImplementacion.setEditable(false);
        }
    }

