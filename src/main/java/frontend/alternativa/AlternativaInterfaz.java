    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnCrearActionPerformed
        String nombre = txtNombre.getText();
        String descripcion = txtDescripcion.getText();
        String pros = txtPros.getText();
        String contras = txtContras.getText();
        String coString = txtCosto.getText();
        if (nombre.isEmpty() || descripcion.isEmpty() || pros.isEmpty() || contras.isEmpty() || coString.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos.", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (alternativa == null && !evaluar) {
            alternativa = new Alternativa();
            alternativa.setNombre(nombre);
            alternativa.setDescripcion(descripcion);
            alternativa.setPros(pros);
            alternativa.setContras(contras);
            alternativa.setCosto(Double.parseDouble(coString));
            alternativa.setTiempoDeImplementacion(Integer.parseInt(txtImplementacion.getText()));
            alternativa.setCategoria(txtCategoria.getSelectedItem().toString());
            alternativa.setIdProyecto(proyecto.getId());
            alternativaControlador.insertar(alternativa);
            this.dispose();
            JOptionPane.showMessageDialog(this, "Alternativa creada con éxito.", "Éxito",
                    JOptionPane.INFORMATION_MESSAGE);
        } else if (alternativa != null && !evaluar) {
            alternativa.setNombre(nombre);
            alternativa.setDescripcion(descripcion);
            alternativa.setPros(pros);
            alternativa.setContras(contras);
            alternativa.setCosto(Double.parseDouble(coString));
            alternativa.setTiempoDeImplementacion(Integer.parseInt(txtImplementacion.getText()));
            alternativa.setCategoria(txtCategoria.getSelectedItem().toString());
            alternativaControlador.actualizar(alternativa);
            this.dispose();
            JOptionPane.showMessageDialog(this, "Alternativa actualizada con éxito.", "Éxito",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            alternativa.setCategoria(txtCategoria.getSelectedItem().toString());
            alternativaControlador.actualizar(alternativa);
            this.dispose();
            JOptionPane.showMessageDialog(this, "Alternativa evaluada con éxito.", "Éxito",
                    JOptionPane.INFORMATION_MESSAGE);
        }

    }// GEN-LAST:event_btnCrearActionPerformed

