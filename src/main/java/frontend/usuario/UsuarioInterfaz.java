    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnCrearActionPerformed
        String nombre = txtNombre.getText();
        String telefono = txtTelefono.getText();
        String password1 = new String(txtPassword1.getPassword());
        String password2 = new String(txtPassword2.getPassword());

        if (nombre.isEmpty() || telefono.isEmpty() || password1.isEmpty() || password2.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios", "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!password1.equals(password2)) {
            javax.swing.JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden", "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (usuario == null) {
            usuario = new Usuario();
            usuario.setNombre(nombre);
            usuario.setTelefono(Integer.parseInt(telefono));
            usuario.setPassword(password1);
            usuario.setAcceso(txtAcceso.getSelectedItem().toString().equals("Administrador"));
            usuarioControlador.insertar(usuario);
            JOptionPane.showMessageDialog(this, "Usuario creado correctamente", "Exito",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            usuario.setNombre(nombre);
            usuario.setTelefono(Integer.parseInt(telefono));
            usuario.setPassword(password1);
            usuario.setAcceso(txtAcceso.getSelectedItem().toString().equals("Administrador"));
            usuarioControlador.actualizar(usuario);
            JOptionPane.showMessageDialog(this, "Usuario actualizado correctamente", "Exito",
                    JOptionPane.INFORMATION_MESSAGE);
        }
        this.dispose();
    }// GEN-LAST:event_btnCrearActionPerformed

