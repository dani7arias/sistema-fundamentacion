    private void cargarDatos(Usuario usuario, boolean ver) {
        this.usuario = usuario;
        if (usuario != null) {
            setTitle("Modificar Usuario");
            jLabel1.setText("Modificar Usuario");
            txtNombre.setText(usuario.getNombre());
            txtTelefono.setText(String.valueOf(usuario.getTelefono()));
            txtPassword1.setText(usuario.getPassword());
            txtPassword2.setText(usuario.getPassword());
            txtAcceso.setSelectedItem(usuario.getAcceso() ? "Administrador" : "Usuario");
            btnCrear.setText("Modificar");
        }
        if (ver) {
            setTitle("Ver Usuario");
            jLabel1.setText("Detalles del Usuario");
            txtNombre.setEditable(false);
            txtTelefono.setEditable(false);
            txtPassword1.setEditable(false);
            txtPassword2.setEditable(false);
            txtAcceso.setEnabled(false);
            btnCrear.setVisible(false);
        }
    }

    private void initControladores() {
        usuarioControlador = new UsuarioControlador();
    }

